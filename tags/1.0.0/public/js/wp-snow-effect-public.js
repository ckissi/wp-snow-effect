(function ($) {
    'use strict';

    $(window).load(function () {

        if(!snoweffect.show) return;
        jQuery().jSnow({
            flakes: snoweffect.flakes_num,
            fallingSpeedMin: snoweffect.falling_speed_min,
            fallingSpeedMax:  snoweffect.falling_speed_max,
            flakeMaxSize:  snoweffect.flake_max_size,
            flakeMinSize: snoweffect.flake_min_size,
            flakeColor: [ snoweffect.flake_color ],
            vSize: snoweffect.vertical_size,
            fadeAway: snoweffect.fade_away
        });
    });

})(jQuery);
